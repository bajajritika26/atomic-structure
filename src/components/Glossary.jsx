
export default function Glossary() {
  return (
    <div className="card">
      <h2>Glossary</h2>
      <ul className="list">
        <li><b>Atom</b>: Smallest unit of matter that retains the properties of an element.</li>
        <li><b>Nucleus</b>: Central core containing protons and neutrons.</li>
        <li><b>Proton (p⁺)</b>: Positively charged particle in the nucleus.</li>
        <li><b>Neutron (n⁰)</b>: Neutral particle in the nucleus.</li>
        <li><b>Electron (e⁻)</b>: Negatively charged particle around the nucleus.</li>
        <li><b>Atomic Number (Z)</b>: Number of protons in the nucleus.</li>
        <li><b>Mass Number (A)</b>: Sum of protons and neutrons.</li>
        <li><b>Shell</b>: Energy level where electrons are likely found (Bohr model).</li>
        <li><b>Excitation</b>: Electron absorbs energy and jumps to a higher shell.</li>
        <li><b>Relaxation</b>: Electron returns to a lower shell, emitting light (photon).</li>
        <li><b>Ion</b>: Charged atom (different number of electrons vs protons).</li>
        <li><b>Isotope</b>: Atoms of the same element with different neutrons.</li>
      </ul>
    </div>
  )
}
